/**
 * @interface IPosition
 * @description Defines the structure of coordinates.
 * @props `lat` `undefined` The latitude of the position
 * @props `lng` `undefined` The longitude of the position
 * @props `x` `undefined` The x coordinate of the position
 * @props `y` `undefined` The y coordinate of the position
 * @props `z` `undefined` The z coordinate of the position
 */
export interface IPosition {
    lat?: number
    lng?: number
    x?: number
    y?: number
    z?: number
}
