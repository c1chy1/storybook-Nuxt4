/**
 * @enum {string} EDividers
 * @description Defines types used for the divider component.
 * @prop BUTTON Has a button within itself to be clicked
 * @prop DEFAULT Default divider being a basic separation line
 * @prop LABEL Labeled divider - has a small text area at the center
 * @prop TITLE Labeled divider - has a larger text area at the center
 */
export enum EDividers {
    BUTTON = 'button',
    DEFAULT = 'default',
    LABEL = 'label',
    TITLE = 'title',
}
