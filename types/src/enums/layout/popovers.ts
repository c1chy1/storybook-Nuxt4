/**
 * @enum {string} EPopovers
 * @description Defines types used for the popover component.
 * @prop CLICK Popover opens when item is clicked
 * @prop HOVER Popover opens when item is hovered
 * @prop NONE Popover does not open with click or hover
 */
export enum EPopovers {
    CLICK = 'click',
    HOVER = 'hover',
    NONE = 'none',
}
